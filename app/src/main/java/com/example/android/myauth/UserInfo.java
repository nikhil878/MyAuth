public class UserInfo {

    public String name;
    public String email;
    public String mobNo;

    public UserInfo()
    {

    }

    public UserInfo(String name, String email, String mobNo) {
        this.name = name;
        this.email = email;
        this.mobNo = mobNo;
    }
}
